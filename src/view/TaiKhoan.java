    //để kiểm tra xem một mã nhân viên idNv có tồn tại trong cơ sở dữ liệu hay không
    private boolean checkIdNhanVien(String idNv, Connect connect) {
        String query = "SELECT * FROM nhanvien WHERE maNV = ?";
        try (PreparedStatement preparedStatement = connect.conn.prepareStatement(query)) {
            preparedStatement.setString(1, idNv);
            try (ResultSet resultSet = preparedStatement.executeQuery()) {
                if (resultSet.next()) {
                    return true;
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
        return false;
    }


