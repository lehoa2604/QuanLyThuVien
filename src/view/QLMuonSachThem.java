    private void btnXacNhanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXacNhanActionPerformed
        if (txtMaMuon.getText().isEmpty() || cboMaSach.getSelectedItem() == null || cboMaDocGia.getSelectedItem() == null || cboMaNhanVien.getSelectedItem() == null || txtNgayMuon.getText().isEmpty() || txtNgayTra.getText().isEmpty() || txtTinhTrangMuon.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin");
    } else {
        // Kiểm tra định dạng ngày mượn và ngày trả
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        dateFormat.setLenient(false);

        try {
            dateFormat.parse(txtNgayMuon.getText());
            dateFormat.parse(txtNgayTra.getText());
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Ngày phải có định dạng yyyy-MM-dd");
            return; // Dừng thực hiện nếu ngày không đúng định dạng
        }

        try {
            Connect connect = new Connect();
            connect.insertMuonSach(txtMaMuon.getText(), cboMaSach.getSelectedItem().toString(), cboMaDocGia.getSelectedItem().toString(), cboMaNhanVien.getSelectedItem().toString(), txtNgayMuon.getText(), txtNgayTra.getText(), txtTinhTrangMuon.getText());
            JOptionPane.showMessageDialog(this, "Bạn đã thêm thành công");
            QLMuonSach qLMuonSach = new QLMuonSach();
            qLMuonSach.setVisible(true);
            this.setVisible(false);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(QLKhoSachThem.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Trùng mã mượn sách. Vui lòng nhập lại mã mượn sách!!");
        }
    }
    }//GEN-LAST:event_btnXacNhanActionPerformed

