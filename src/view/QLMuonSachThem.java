    //lấy danh sách dữ liệu từ cột cụ thể trong bảng cụ thể nào đó từ csdl
    private ArrayList<String> DuLieuTuCot(String bang, String cot) throws ClassNotFoundException, SQLException {
        ArrayList<String> columnDataList = new ArrayList<>();
        Connect connect = new Connect();
        ResultSet rs = connect.getDataFromTable(bang, cot);

        while (rs.next()) {
            String columnValue = rs.getString(cot);
            columnDataList.add(columnValue);
        }
        rs.close();
        return columnDataList;
    }

    //đổ dữ liệu đó vào các combobox tương ứng để hiển thị
    private void DuLieuVaoCombobox() {
        try {
            ArrayList<String> maSachList = DuLieuTuCot("sach", "maSach");
            ArrayList<String> maDGList = DuLieuTuCot("docgia", "maDG");
            ArrayList<String> maNVList = DuLieuTuCot("nhanvien", "maNV");
            
            // Xóa các mục hiện có trong JComboBoxes
            cboMaSach.removeAllItems();
            cboMaDocGia.removeAllItems();
            cboMaNhanVien.removeAllItems();

            // Đẩy dữ liệu vào JComboBoxes
            for (String maSach : maSachList) {
                cboMaSach.addItem(maSach);
            }

            for (String maDG : maDGList) {
                cboMaDocGia.addItem(maDG);
            }
            
            for (String maNV : maNVList) {
                cboMaNhanVien.addItem(maNV);
            }
        } catch (ClassNotFoundException | SQLException ex) {
            ex.printStackTrace();
        }
    }

    private void HienThiDuLieuMaSach() throws ClassNotFoundException, SQLException {
        Connect connect = new Connect();
        String selectedMaSach = (String) cboMaSach.getSelectedItem(); // Lấy giá trị mã sách từ combobox

        try {
            ResultSet rs = connect.getData("sach", " WHERE maSach = '" + selectedMaSach + "'"); // Truy vấn cơ sở dữ liệu

            if (rs.next()) {
                String tieuDeSach = rs.getString("tieuDeSach");
                String tenTacGia = rs.getString("tenTacGia");
                String nhaXuatBan = rs.getString("nhaXuatBan");
                int namXuatBan = rs.getInt("namXuatBan");
                String theLoai = rs.getString("theLoai");
                
                txtTieuDeSach.setText(tieuDeSach);
                txtTenTacGia.setText(tenTacGia);
                txtNhaXB.setText(nhaXuatBan);
                txtNamXB.setText(String.valueOf(namXuatBan)); // Gán giá trị vào thành phần txtNamXuatBan (ép kiểu int thành String)
                txtTheLoai.setText(theLoai); 
            }
        } catch (SQLException e) {
            e.printStackTrace();
        } 
    }
    
    private void HienThiDuLieuMaDocGia() throws ClassNotFoundException, SQLException {
    Connect connect = new Connect();
    String selectedMaDocGia = (String) cboMaDocGia.getSelectedItem(); // Lấy giá trị mã độc giả từ combobox

    try {
        ResultSet rs = connect.getData("docgia", " WHERE maDG = '" + selectedMaDocGia + "'"); // Truy vấn cơ sở dữ liệu

        if (rs.next()) {
            String tenDG = rs.getString("tenDG");
            String cccdDG = rs.getString("lop");
            int tuoiDG = rs.getInt("tuoiDG");
            String diaChiDG = rs.getString("diaChiDG");
            String sdtDG = rs.getString("sdtDG");
            String emailDG = rs.getString("emailDG");

            txtTenDocGia.setText(tenDG);
            txtCanCuoc.setText(cccdDG);
            txtTuoi.setText(String.valueOf(tuoiDG)); // Gán giá trị vào thành phần txtTuoiDG (ép kiểu int thành String)
            txtDiaChi.setText(diaChiDG);
            txtSDT.setText(sdtDG);
            txtEmail.setText(emailDG);
        }
    } catch (SQLException e) {
        e.printStackTrace();
    } 
}

