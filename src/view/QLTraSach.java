    private void btnXoaTraSachActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaTraSachActionPerformed
        int selectedRow = tbTraSach.getSelectedRow();
        if (selectedRow == -1) {
            // Không có hàng nào được chọn
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một hàng để xoá.", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return;
        } else {
            String maTra = tbTraSach.getValueAt(selectedRow, 0).toString();
            // Hiển thị hộp thoại xác nhận
            int choice = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xoá hàng đã chọn?", "Xác nhận xoá", JOptionPane.YES_NO_OPTION);
            if (choice == JOptionPane.YES_OPTION) {
                try {
                    Connect connect = new Connect();
                    connect.DeleteTraSach(maTra); // Gọi phương thức xóa hàng từ CSDL

                    // Xoá hàng trong mô hình bảng
                    DefaultTableModel model = (DefaultTableModel) tbTraSach.getModel();
                    model.removeRow(selectedRow);

                    JOptionPane.showMessageDialog(this, "Xoá thành công.", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "Đã xảy ra lỗi khi xoá hàng.\n" + ex.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_btnXoaTraSachActionPerformed

    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        if (txtTimKiemTraSach.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập từ khóa tìm kiếm!!");
        } else {
            try {
                Connect ts = new Connect();
                ResultSet rs = null;

                // Xây dựng câu truy vấn với điều kiện LIKE
                String keyword = txtTimKiemTraSach.getText();
                String query = " WHERE "
                        + "maTra LIKE '%" + keyword + "%' OR "
                        + "maMuon LIKE '%" + keyword + "%' OR "
                        + "ngayTraThuc LIKE '%" + keyword + "%' OR "
                        + "tinhTrangTra LIKE '%" + keyword + "%' OR "
                        + "khoanThu LIKE '%" + keyword + "%' OR "
                        + "ghiChu LIKE '%" + keyword + "%'";

                rs = ts.getData("trasach", query);
                DefaultTableModel model = (DefaultTableModel) tbTraSach.getModel();
                model.setRowCount(0);
                while (rs.next()) {
                    Object[] data = {rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getString(5), rs.getString(6)};
                    model.addRow(data);
                }
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập đúng định dạng", "Lỗi", JOptionPane.ERROR_MESSAGE);
            } catch (Exception e) {

            }
        }
    }//GEN-LAST:event_btnTimKiemActionPerformed

