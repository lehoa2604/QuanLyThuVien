    private void btnSuaTraSachActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaTraSachActionPerformed
        int selectedRow = tbTraSach.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng cần sửa");
        } else {
            // Lấy thông tin từ dòng được chọn (lấy giá trị của các cột trong bảng )
            String maTra = tbTraSach.getValueAt(selectedRow, 0).toString();
            String maMuon = tbTraSach.getValueAt(selectedRow, 1).toString();
            String ngayTraThuc = tbTraSach.getValueAt(selectedRow, 2).toString();
            String tinhTrangTra = tbTraSach.getValueAt(selectedRow, 3).toString();
            String khoanThu = tbTraSach.getValueAt(selectedRow, 4).toString();
            String ghiChu = tbTraSach.getValueAt(selectedRow, 5).toString();

            // Mở form QLKhoSachSua và truyền thông tin vào các JTextField trên form đó
            QLTraSachSua qLTraSachXem = new QLTraSachSua();
            qLTraSachXem.setValues(maTra, maMuon, ngayTraThuc, tinhTrangTra, khoanThu, ghiChu);
            qLTraSachXem.setVisible(true);
        }
    }//GEN-LAST:event_btnSuaTraSachActionPerformed

