    //đếm số lượng mã sách trong bảng muonSach của cơ sở dữ liệu dựa trên một mã sách cụ thể (masach).
    private int countBookIds(Connect connect, String masach) {
        int count = 0;
        String query = "SELECT COUNT(maSach) AS tongSo FROM muonSach WHERE maSach = ?";

        try (PreparedStatement preparedStatement = connect.conn.prepareStatement(query)) {
            preparedStatement.setString(1, masach);
            try (ResultSet resultSet = preparedStatement.executeQuery()) {
                if (resultSet.next()) {
                    count = resultSet.getInt("tongSo"); //giá trị của cột "tongSo" được lấy ra và gán cho biến count.
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }

        return count;
    }

