    //chuyển đổi một chuỗi ngày String thành đối tượng LocalDate
    public static LocalDate chuyenDoiDate(String dateString) {
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd"); //Tạo một đối tượng sử dụng phương thức tĩnh ofPattern của lớp DateTimeFormatter.
        return LocalDate.parse(dateString, formatter); //chuyển đổi chuỗi dateString thành một đối tượng LocalDate
    }

    // tính toán số ngày chênh lệch giữa hai đối tượng ngayTraThuc và ngayHenTra, Sử dụng phương thức tĩnh between của lớp ChronoUnit để tính toán số ngày chênh lệch giữa ngayHenTra và ngayTraThuc
    public static int tinhKhoangCachNgay(LocalDate ngayTraThuc, LocalDate ngayHenTra) {
        long daysBetween = ChronoUnit.DAYS.between(ngayHenTra, ngayTraThuc);
        return (int) daysBetween;
    }

    private int tinhKhoanThu(int soNgayTra, int tinhTrangTra, int giaTien) {
        if (soNgayTra > 0 && tinhTrangTra == 0) {
            return soNgayTra * 2000;
        } else if (soNgayTra <= 0 && tinhTrangTra > 0) {
            return (int) ((100 - tinhTrangTra) / 100.0 * giaTien);// Sử dụng (100 - tinhTrangTra) / 100.0 sẽ chuyển đổi giá trị phần trăm sang dạng thập phân để tính toán đúng giá trị phần trăm của giaTien.
        } else if (soNgayTra > 0 && tinhTrangTra > 0) {
            return soNgayTra * 2000 + (int) ((100 - tinhTrangTra) / 100.0 * giaTien);
        } else {
            return 0;
        }
    }

    private String tinhGhiChu(int soNgayTra, int tinhTrang, int giaTien) {
        int tinhTrangTra = 100 - tinhTrang;
        if (soNgayTra > 0 && tinhTrangTra == 0) {
            return "Quá hạn " + soNgayTra + " ngày";
        } else if (soNgayTra <= 0 && tinhTrangTra > 0) {
            return "Đúng hạn \nnhưng sách hỏng " + tinhTrangTra + "% của sách có giá " + giaTien;
        } else if (soNgayTra > 0 && tinhTrangTra > 0) {
            return "Quá hạn " + soNgayTra + " ngày và \n sách hỏng " + tinhTrangTra + "% của sách có giá " + giaTien;
        } else {
            return "Đúng hạn và sách không hỏng gì";
        }
    }

    private void show_Table() {
        DefaultTableModel model = (DefaultTableModel) tbTraSach.getModel();
        try {
            Connect ts = new Connect();
            ResultSet rs = ts.getData("trasach", "");
            model.setRowCount(0);
            QLMuonSach qlMuonSach = new QLMuonSach();

            while (rs.next()) {
                Object maTra = rs.getString(1);
                Object maMuon = rs.getString(2);
                String ngayTraThuc = rs.getString(3);
                String tinhTrang = rs.getString(4);

                String ngayHenTra = qlMuonSach.getNgayHenTraByMaMuon((String) maMuon);
                LocalDate ngayTraThucDate = chuyenDoiDate(ngayTraThuc);
                LocalDate ngayHenTraDate = chuyenDoiDate(ngayHenTra);

                int giaTien = qlMuonSach.getGiaTienByMaMuon((String) maMuon);
                int soNgayTra = tinhKhoangCachNgay(ngayTraThucDate, ngayHenTraDate);
                int tinhTrangTra = Integer.parseInt(tinhTrang); // Chuyển đổi tinhTrang thành kiểu int

                int khoanThu = tinhKhoanThu(soNgayTra, tinhTrangTra, giaTien);
                String ghiChu = tinhGhiChu(soNgayTra, tinhTrangTra, giaTien); // Lấy ghi chú dựa trên các trạng thái

                Object[] data = {maTra, maMuon, ngayTraThuc, tinhTrang, khoanThu, ghiChu}; // Thêm ghi chú vào dữ liệu cột
                model.addRow(data);
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }
