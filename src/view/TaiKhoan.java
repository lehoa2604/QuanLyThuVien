    private void btnTaoTKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTaoTKActionPerformed
        if (txtDangNhap.getText().isEmpty() || txtMatKhau.getText().isEmpty() || txtMatKhauNL.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin");
        } else {
            try {
                Connect connect = new Connect();

                Boolean checkId = checkIdNhanVien(txtDangNhap.getText(), connect);
                if (!checkId) {
                    JOptionPane.showMessageDialog(this, "Mã NV của bạn không chính xác");
                } else {
                    String matKhau = txtMatKhau.getText();
                    String matKhauNhapLai = txtMatKhauNL.getText();

                    if (matKhau.equals(matKhauNhapLai)) {
                        connect.insertAccount(txtDangNhap.getText(), matKhau);
                        JOptionPane.showMessageDialog(this, "Tạo tài khoản thành công!");
                        DangNhap dangNhap = new DangNhap();
                        dangNhap.setVisible(true);
                    } else {
                        JOptionPane.showMessageDialog(this, "Mật khẩu không trùng khớp!");
                    }
                }

            } catch (Exception e) {
                System.out.println(e);
            }
        }
    }//GEN-LAST:event_btnTaoTKActionPerformed

