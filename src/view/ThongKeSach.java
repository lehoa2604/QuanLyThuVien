    private void show_Table() {
        DefaultTableModel model = (DefaultTableModel) tbSach.getModel();
        try {
            Connect ts = new Connect();
            ResultSet rs = ts.getData("sach", "");
            model.setRowCount(0);

            while (rs.next()) {
                Object maSach = rs.getString(1);
                Object tieDeSach = rs.getString(2);
                Object theLoai = rs.getString(6);
                Object soLuong = rs.getString(7);
                int soLuongMuon = countBookIds(ts, maSach.toString()); // Lấy số lượng sách mượn theo maSach
                int soLuongConLai = Integer.parseInt(soLuong.toString()) - soLuongMuon; // Tính số lượng sách còn lại

                Object[] data = {maSach, tieDeSach, theLoai, soLuong, soLuongMuon, soLuongConLai}; // Thêm số lượng sách còn lại vào dữ liệu
                model.addRow(data);
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }

    //tổng số lượng sách trong bảng sach của cơ sở dữ liệu
    private int calculateTotalQuantity() throws ClassNotFoundException {
        int totalQuantity = 0;
        try {
            Connect connect = new Connect();
            Statement statement = connect.conn.createStatement();

            String query = "SELECT SUM(soLuong) AS soLuong FROM sach"; // Truy vấn tổng số lượng sách
            ResultSet resultSet = statement.executeQuery(query);

            if (resultSet.next()) {
                totalQuantity = resultSet.getInt("soLuong"); // Lấy giá trị tổng từ cột "soLuong"
            }

            resultSet.close();
            statement.close();
            connect.conn.close(); // Đóng kết nối cơ sở dữ liệu
        } catch (SQLException e) {
            e.printStackTrace();
        }
        return totalQuantity;
    }

    
    // lấy danh sách các mã sách từ bảng sach trong cơ sở dữ liệu
    private static List<String> getBookIds(Connect connect) {
        List<String> bookIds = new ArrayList<>();
        String query = "SELECT maSach FROM sach";

        try (Statement statement = connect.conn.createStatement();
                ResultSet resultSet = statement.executeQuery(query)) {

            while (resultSet.next()) {
                bookIds.add(resultSet.getString("maSach"));
            }

        } catch (SQLException e) {
            e.printStackTrace();
        }

        return bookIds;
    }
